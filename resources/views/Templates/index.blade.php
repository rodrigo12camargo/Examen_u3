									<h3>La evolucion del telefono</h3>
									<p>Para avanzar con esta lección sobre la historia del teléfono y su evolución debemos dialogar sobre las distintas etapas por las que ha pasado el teléfono y cómo ha ido evolucionando. Durante cada año a partir de la construcción del teléfono fueron mucho los adelantos, varios de ellos para mejorar la explotación de las redes telefónicas. El teléfono fijo, es decir, aquella que se basa en la comunicación de líneas entre telefónicos no portables, los cuales están conectados gracias a un sistema central. <br/>
										-Creación de una central telefónica que permite la interconexión. Esta fue la primera red, la cual se realizaba de forma manual gracias a la intervención de una operadora. <br/>
										-Centrales telefónicas que permiten la interconexión, pero de forma automática en lugar de manual. <br/>
										-Centrales digitales de conmutación automáticas que funcionan mediante el control de un ordenador.<br/>
										-Nuevos medios que aumentan la velocidad del intercambio de datos, como por ejemplo la RDSI o la banda ancha.<br/>
										-La creación de los teléfonos móviles, que permiten el intercambio de voz y datos de forma totalmente inalámbrica.</p>
