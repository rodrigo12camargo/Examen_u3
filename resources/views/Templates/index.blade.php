									<p>Antonio Meucci era un desarrollador italiano experto en la zona de la electrodinámica. Meucci diseñó la invención para comunicar su oficina, situada en la planta de debajo, con su alcoba, ubicado en el segundo piso. Empero Meucci no patentó su invento por 2 causas. La primera era que el creador italiano no poseía mucho dinero para abonar la patente, y no estaba seguro de las ventajas que el teléfono podía darle. En la demostración el creador italiano transmitió la voz de una vocalista italiana a una enorme distancia, por calamidad nadie entregó una buena oferta a Meucci por el invento. El desarrollador poseía temor de que alguien comprara la patente de su invento, debido a que él era incapaz de costear el costo de patentarla de manera definitiva. Meucci busco una forma no definitiva, patentando la invención de manera anual, algo que sí que se podía permitir costear.</p>
