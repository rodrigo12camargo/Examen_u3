									<p>En 1876, un creador del Reino Unido denominado Alexander Graham Bell registró la patente del teléfono. Meucci no tardó un largo tiempo en saber, y solicitó a su jurista que reclamara frente a la Oficina de Patentes, debido a que el desarrollador italiano ya había patentado su invento aunque fuera de manera anual. Poco después Meucci encontró, debido a un amigo que trabajaba en Washington, que toda la documentación destinada a la patente de Meucci se había perdido, por lo cual no existían pruebas de que la patente del teléfono fuera de Meucci previo a que la registrara Graham Bell. La compañía de Bell era bastante poderosa y se encontró que había sobornado tanto a la Oficina de Patentes como al propio jurista de Meucci. En el imaginario exitosa constantemente fue Graham Bell el desarrollador del teléfono, sin embargo en los últimos años la tendencia está cambiando, dándole aquel reconocimiento a Meucci.</p>
